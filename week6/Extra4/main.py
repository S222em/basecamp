def count_sales_of(puzzle: str, sales: list[tuple[int, str, str]]) -> int:
    return sum(sale[0] for sale in sales if sale[1] == puzzle)


def count_sales_per(sales: list[tuple[int, str, str]]) -> dict[str, int]:
    sales_per = dict()

    for amount, name, _ in sales:
        sales_per.setdefault(name, 0)
        sales_per[name] += amount

    return sales_per


